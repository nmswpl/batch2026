public class ComputeQuotientRemainder {
    public static void main(String[] args) {
        int a = 4;
        int b = 2;
        int quotient = a / b;
        int remainder = a % b;
        System.out.println("Quotient = "+quotient+"\nRemainder = "+remainder);
    }
}
